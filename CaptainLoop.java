public class CaptainLoop extends Maestro
{
	private Pregunta[] preguntas;
	private Respuesta[] respuestas;
	private int index;
	
	public CaptainLoop(int vida, int ataque)
	{
		super(vida, ataque);
	}
	
	public void setIndex(int index)
	{
		this.index = index;
	}
	
	public void preguntasProgra(Pregunta[] preguntas, int index)
	{
		preguntas.getPreguntasProgra[index];
	}
	
	public void respuestasProgra0(Respuesta[] respuesta, int index)
	{
		respuestas.getRespuestasProgra0[index];
	}
	
	public void respuestasProgra1(Respuesta[] respuesta, int index)
	{
		respuestas.getRespuestasProgra1[index];
	}
	
	public void respuestasProgra2(Respuesta[] respuesta, int index)
	{
		respuestas.getRespuestasProgra2[index];
	}
	
	
	
	
}
