public class PorfirioGuiaz extends Maestro
{
	private Pregunta[] preguntas;
	private Respuesta[] respuestas;
	private int index;
	
	public PorfirioGuiaz(int vida, int ataque)
	{
		super(vida,ataque);
	}
	
	public void setIndex(int index)
	{
		this.index = index;
	}
	
	public void preguntasHisto(Pregunta[] preguntas, int index)
	{
		preguntas.getPreguntasHisto[index];
	}
	
	public void respuestasHisto0(Respuesta[] respuesta, int index)
	{
		respuestas.getPreguntasHisto0[index];
	}
	
	public void respuestasHisto1(Respuesta[] respuesta, int index)
	{
		respuestas.getPreguntasHisto1[index];
	}
	
	public void respuestasHisto2(Respuesta[] respuesta, int index)
	{
		respuestas.getPreguntasHisto2[index];
	}
	
	
}
